package Class1;


public class clsEscapeSequence {
    public static void main(String[] args) {
        System.out.println("Hellow world");
        System.out.println("Tab Space" + "Hellow \t world");
        System.out.println("New line:" + "Hellow \n world");
        System.out.println("Double quote string line:" + "Hellow \" world\"");
        System.out.println("Single Slash:" + "Hellow \\ world");

    }
}
